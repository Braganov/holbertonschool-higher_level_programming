/**
 * Tâche 0: Color Me
 * 
 * Ce script modifie la couleur du texte de l'élément header en rouge (#FF0000)
 * en utilisant la méthode querySelector pour sélectionner l'élément.
 * 
 * querySelector: Méthode qui permet de sélectionner le premier élément 
 * correspondant au sélecteur CSS spécifié.
 */

